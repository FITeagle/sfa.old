package org.fiteagle.sfa.common;

public class GeniSlivers {

	private String geni_sliver_urn;
	private String geni_expires;
	private String geni_allocation_status;
	private String geni_operational_status;
	private String geni_error;

	public String getGeni_sliver_urn() {
		return geni_sliver_urn;
	}

	public void setGeni_sliver_urn(String geni_sliver_urn) {
		this.geni_sliver_urn = geni_sliver_urn;
	}

	public String getGeni_expires() {
		return geni_expires;
	}

	public void setGeni_expires(String geni_expires) {
		this.geni_expires = geni_expires;
	}

	public String getGeni_allocation_status() {
		return geni_allocation_status;
	}

	public void setGeni_allocation_status(String geni_allocation_status) {
		this.geni_allocation_status = geni_allocation_status;
	}

	public String getGeni_operational_status() {
		return geni_operational_status;
	}

	public void setGeni_operational_status(String geni_operational_status) {
		this.geni_operational_status = geni_operational_status;
	}

	public String getGeni_error() {
		return geni_error;
	}

	public void setGeni_error(String geni_error) {
		this.geni_error = geni_error;
	}

}
